package com.cybertrisquad.availloans;

public class UserProfile {
    String string_username;
    String string_age;
    String string_contactno;
    String string_email;

    public String getString_username() {
        return string_username;
    }

    public void setString_username(String string_username) {
        this.string_username = string_username;
    }

    public String getString_age() {
        return string_age;
    }

    public void setString_age(String string_age) {
        this.string_age = string_age;
    }

    public String getString_contactno() {
        return string_contactno;
    }

    public void setString_contactno(String string_contactno) {
        this.string_contactno = string_contactno;
    }

    public String getString_email() {
        return string_email;
    }

    public void setString_email(String string_email) {
        this.string_email = string_email;
    }
}
